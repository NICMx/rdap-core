package mx.nic.rdap.core.db;

public class DomainLabelException extends Exception {

	private static final long serialVersionUID = 7954965751436962915L;

	public DomainLabelException() {
		super();
	}

	public DomainLabelException(String message, Throwable cause) {
		super(message, cause);
	}

	public DomainLabelException(String message) {
		super(message);
	}

	public DomainLabelException(Throwable cause) {
		super(cause);
	}

}
